#include		"fileRequestManager.hh"

// t e s t . t x t
// 0 1 2 3 4 5 6 7
// size : 8

// resize : index('.') = 4

// t e s t
// 0 1 2 3
// size : 4

void			ef::FileRequestManager::removeExtension(std::string	&	str) const
{
  size_t		i;

  for (i = str.size() - 1; i > 0; i -= 1)
    {
      if (str[i] == '.')
	str.resize(i);
    }
}
